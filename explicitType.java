public class explicitType {
    public static void main(String[] args) {
        // Explicit type declaration

        float myFloat = 5;
        System.out.println(myFloat); // Output: 5.0

        int myInt = (int) 5.42f; // int type
        System.out.println(myInt);

        long ebig = (long) 3.45;
        System.out.println(ebig);
    }
}
